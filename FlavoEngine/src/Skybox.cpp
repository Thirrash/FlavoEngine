Skybox::Skybox() {
	std::vector<std::string> faces {
		"../../Resources/Skybox/Left.jpg",
		"../../Resources/Skybox/Right.jpg",
		"../../Resources/Skybox/Bottom.jpg",
		"../../Resources/Skybox/Top.jpg",
		"../../Resources/Skybox/Front.jpg",
		"../../Resources/Skybox/Back.jpg"
	};
	
	textureId_ = LoadCubemap(faces);
	InitBuffers();

	shaderIndex = Framework::FUtils::CompileShader("../../Resources/Shaders/Skybox.vert", "../../Resources/Shaders/Skybox.frag");
}

unsigned int Skybox::LoadCubemap(std::vector<std::string> TexturePaths) {
	unsigned int textureID;
	glGenTextures(1, &textureID);
	glBindTexture(GL_TEXTURE_CUBE_MAP, textureID);

	int width, height, nrChannels;
	for (unsigned int i = 0; i < 6; i++) {
		int width, height, noChannels;
		unsigned char* data = Framework::FUtils::LoadTextureFromFile(TexturePaths[i], &width, &height, &noChannels);
		if (data) {
			glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGB, width, height, 0, GL_RGB, GL_UNSIGNED_BYTE, data);
		} else {
			LogR("Loaded image data is null", TexturePaths[i]);
		}

		Framework::FUtils::DeallocateTextureFile(data);
	}
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);

	return textureID;
}

